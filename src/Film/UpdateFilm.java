    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        if (txtJudul.getText().equals("") || txtDeskripsi.getText().equals("") || txtMinUmur.getText().equals("") || txtHarga.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Harap lengkapi semua data!");
        } else {
            try {
                sql = "UPDATE tbl_film SET "
                        + "judul='" + txtJudul.getText() + "',"
                        + "deskripsi='" + txtDeskripsi.getText() + "',"
                        + "sutradara='" + txtSutradara.getText() + "',"
                        + "produser='" + txtProduser.getText() + "',"
                        + "min_umur='" + txtMinUmur.getText() + "',"
                        + "harga_jual='" + txtHarga.getText() + "' "
                        + "WHERE id_film='" + txtID.getText() + "'";
                stat.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Berhasil update data!");
                btnUpdate.setEnabled(false);
                kosong();
                load_table();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Gagal update data " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed


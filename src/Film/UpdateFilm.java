    private void tableDataMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableDataMouseClicked
        // TODO add your handling code here:
        btnUpdate.setEnabled(true);
        int baris = tableData.rowAtPoint(evt.getPoint());
        txtID.setText(tableData.getValueAt(baris, 0).toString());
        txtJudul.setText(tableData.getValueAt(baris, 1).toString());
        txtDeskripsi.setText(tableData.getValueAt(baris, 2).toString());
        txtSutradara.setText(tableData.getValueAt(baris, 3).toString());
        txtProduser.setText(tableData.getValueAt(baris, 4).toString());
        txtMinUmur.setText(tableData.getValueAt(baris, 5).toString());
        txtHarga.setText(tableData.getValueAt(baris, 6).toString());
    }//GEN-LAST:event_tableDataMouseClicked

    private void txtCariKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCariKeyPressed
        // TODO add your handling code here:
        if (txtCari.getText().isEmpty()) {
            load_table();
        } else {
            DefaultTableModel table = new DefaultTableModel();
            table.addColumn("ID");
            table.addColumn("Judul Film");
            table.addColumn("Deskripsi");
            table.addColumn("Sutradara");
            table.addColumn("Produser");
            table.addColumn("Min Umur");
            table.addColumn("Harga Jual");
            try {
                sql = "SELECT * FROM tbl_film WHERE judul LIKE '%" + txtCari.getText() + "%' "
                        + "ORDER BY judul";
                res = stat.executeQuery(sql);
                while (res.next()) {
                    table.addRow(new Object[]{
                        res.getString("id_film"),
                        res.getString("judul"),
                        res.getString("deskripsi"),
                        res.getString("sutradara"),
                        res.getString("produser"),
                        res.getString("min_umur"),
                        res.getString("harga_jual"),});
                }
                tableData.setModel(table);
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage());
            }
        }
    }//GEN-LAST:event_txtCariKeyPressed

